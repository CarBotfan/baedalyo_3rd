package com.green.beadalyo.gyb.common.exception;

public class DataWrongException extends Exception
{
    public DataWrongException() {super();}
    public DataWrongException(String message) {super(message);}
    public DataWrongException(String message, Throwable throwable) {super(message,throwable);}
    public DataWrongException(Throwable throwable) {super(throwable);}
}
